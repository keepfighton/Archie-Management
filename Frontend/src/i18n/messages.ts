export function normalizeLocale(locale: string | null | undefined): SupportedLocale {
  if (locale && SUPPORTED_LOCALES.includes(locale as SupportedLocale)) {
    return locale as SupportedLocale
  }
  return DEFAULT_LOCALE
}

export function getLanguageFromLocale(locale: string): SupportedLanguage {
  return locale.toLowerCase().startsWith('id') ? 'id' : 'en'
}

export function translate(
  locale: string,
  key: string,
  fallback?: string,
  variables?: Record<string, string | number>
) {
  const template = messages[getLanguageFromLocale(locale)][key] ?? fallback ?? key

  if (!variables) return template

  return template.replace(/\{(\w+)\}/g, (_, token: string) => String(variables[token] ?? `{${token}}`))
}
